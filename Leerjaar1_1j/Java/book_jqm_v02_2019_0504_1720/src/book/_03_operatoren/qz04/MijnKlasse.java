package book._03_operatoren.qz04;

public class MijnKlasse
{
  public static void main(String[] args)
  {
    int x = 3;
    int y = 8;
    int z = 3;
    
    if (x == z)
    {
      System.out.print("N");
    }
    if (x >= y)
    {
      System.out.print("O");
    }
    if (x <= z)
    {
      System.out.print("P");
    }
    if (z > y)
    {
      System.out.print("Q");
    }
    if (y != z)
    {
      System.out.print("R");
    }
  }
}
