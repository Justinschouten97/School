package book._10_array_arrayList.ex._03;

public class MijnKlasse
{
  public static void main(String[] args)
  {
    int[] mijnArray = new int[7];
    mijnArray[0] = 7;
    mijnArray[3] = 4;
    mijnArray[5] = 1;
    for (int i = 0; i < mijnArray.length; i++)
    {
      System.out.print(mijnArray[i]);
    }
  }
}
