package book._04_conditionele_statements.qz03;

public class MijnKlasse
{
  public static void main(String[] args)
  {
    char c1 = 'g';
    char c2 = 'h';
    
    if (c1 == 'k')
    {
      System.out.print("w");
    }
    if (c2 == 'h')
    {
      System.out.print("x");
      System.out.print("p");
    }
    if (c1 != c2)
    {
      System.out.print("y");
    }
    else
    {
      System.out.print("z");
    }
  }
}
